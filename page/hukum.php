<h1>
Dasar Hukum Pelayanan Banjarbaru
</h1>
<div class="hero-unit">
<ul>
	<li>						
	Menurut UU Pelayanan Publik Nomor 25 Tahun 2009 pasal 1
	menyebutkan bahwa pelayanan publik adalah kegiatan atau
	rangkaian kegiatan dalam rangka  pemenuhan kebutuhan dasar sesuai
	dengan hak-hak sipil setiap warga negara dan penduduk atas  suatu barang,
	jasa, dan atau pelayanan administrasi yang di sediakan oleh penyelenggara pelayanan publik.
	</li>
	<li>
	Dalam hal peningkatan kualitas pelayanan kepada masyarakat,
	pemerintah telah mengeluarkan kebijakan berupa Keputusan Menteri
	PAN yaitu menurut Kep/25/M.PAN/2/2004 yang kemudian dikembangkan
	menjadi 14 unsur yang “relevan”,”valid” dan “reliabel”.
	</li>
	<li>
	Peraturan Menteri Pendayagunaan Aparatur Negara Dan Reformasi Birokrasi
	Republik Indonesia Nomor 16 tahun 2014 tentang pedoman survei kepuasan
	masyarakat terhadap penyelenggaraan pelayanan publik menyebutkan pada
	pasal 1 dan 2 sebagai berikut:
	<br> Pasal 1 <br>
	<ol type="1">
	<li> Survei Kepuasan Masyarakat adalah pengukuran secara komprehensif kegiatan
	     tentang tingkat kepuasan masyarakat yang diperoleh dari hasil pengukuran atas
	     pendapat masyarakat dalam memperoleh pelayanan dari penyelenggara pelayanan publik. </li>
	<li> Unit pelayanan publik adalah unit/kerja kantor pelayanan pada instansi pemerintah,
	     yang secara langsung maupun tidak langsung memberikan pelayanan kepada penerima pelayanan.</li>
	<li> Unsur Survei Kepuasan Masyarakat adalah faktor dan aspek yang dijadikan pengukuran
	     kepuasan masyarakat terhadap penyelenggaraan pelayanan publik.</li>
	<li> Menteri adalah Menteri yang bertanggung jawab di bidang Pendayagunaan Aparatur Negara dan Reformasi Birokrasi.</li>
	</ol>
	Pasal 2
	<ol type="1">
	<li> Penyelenggara pelayanan publik wajib melakukan Survei Kepuasan Masyarakat secara berkala minimal 1 (satu) kali setahun.</li>
	<li> Apabila dibutuhkan, Survei Kepuasan Masyarakat sebagaimana dimaksud dalam Pasal 1,
	     dapat dilengkapi survei secara seketika setelah mendapat pelayanan.</li>
	</li>

</ul>
</div>
				